package ir.alihatamitajik.saroyal;

public class ColorEntity {
    int modeClock;
    int ffMode;
    int color60;
    int color24;
    int color12;
    int color12N;
    int brightness;

    public int getModeClock() {
        return modeClock;
    }

    public void setModeClock(int modeClock) {
        this.modeClock = modeClock;
    }

    public int getFfMode() {
        return ffMode;
    }

    public void setFfMode(int ffMode) {
        this.ffMode = ffMode;
    }

    public int getColor60() {
        return color60;
    }

    public void setColor60(int color60) {
        this.color60 = color60;
    }

    public int getColor24() {
        return color24;
    }

    public void setColor24(int color24) {
        this.color24 = color24;
    }

    public int getColor12() {
        return color12;
    }

    public void setColor12(int color12) {
        this.color12 = color12;
    }

    public int getColor12N() {
        return color12N;
    }

    public void setColor12N(int color12N) {
        this.color12N = color12N;
    }

    public int getBrightness() {
        return brightness;
    }

    public void setBrightness(int brightness) {
        this.brightness = brightness;
    }
}
